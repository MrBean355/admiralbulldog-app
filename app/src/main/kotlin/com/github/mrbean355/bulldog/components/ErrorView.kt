package com.github.mrbean355.bulldog.components

import androidx.compose.foundation.Image
import androidx.compose.foundation.layout.Arrangement
import androidx.compose.foundation.layout.Column
import androidx.compose.foundation.layout.size
import androidx.compose.material.MaterialTheme
import androidx.compose.material.OutlinedButton
import androidx.compose.material.Text
import androidx.compose.material.icons.Icons
import androidx.compose.material.icons.filled.Warning
import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.ColorFilter
import androidx.compose.ui.graphics.vector.rememberVectorPainter
import androidx.compose.ui.unit.dp
import com.github.mrbean355.bulldog.localization.getString

@Composable
fun ErrorView(
    onTryAgainClick: () -> Unit,
    modifier: Modifier = Modifier
) {
    Column(
        verticalArrangement = Arrangement.spacedBy(8.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        modifier = modifier
    ) {
        Image(
            painter = rememberVectorPainter(Icons.Default.Warning),
            colorFilter = ColorFilter.tint(MaterialTheme.colors.secondary),
            contentDescription = getString("component.error.image.description"),
            modifier = Modifier.size(48.dp)
        )
        Text(text = getString("component.error.message"))
        OutlinedButton(onClick = onTryAgainClick) {
            Text(text = getString("component.error.action"))
        }
    }
}